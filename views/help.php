<div class="help-text">
    <h2>Stuck?</h2>

    <h3>Logging in</h3>

    <p>Press the red button "Sign in with Google in order to sign in.<br>
    To log out, press the log out button that is below the email address.</p>

    <h3>Drag & Drop</h3>

    <dl>
        <dt>To open/close this menue</dt>

        <dd>Click on the triple cube icon to toggle.</dd>

        <dt>To run command</dt>

        <dd>Click and drag instruction on the left into the grey rectangle
        labeled workspace.</dd>

        <dt>To move character</dt>

        <dd>Type in non-negative number into boxes next to label "set x" to
        shift right, and "set y" to shift up.<br>
        Type a non-negative number in the box next to "move".</dd>

        <dt>To show/hide character</dt>

        <dd>First drag "hide" into the grey rectangle to make the pikachu
        disappear. To make it reappear, drag in command "show" after command
        "hide".</dd>

        <dt>Change costume/Background</dt>

        <dd>Click the drop down menue and select your choice!</dd>

        <dt>Repeat</dt>

        <dd>To repeat an action a number of times, type a non-negative number
        into the box next to repeat.</dd>

        <dt>Delete Action</dt>

        <dd>Drag the action from the grey rectangle into the dustbin to delete
        action.</dd>

        <dt>Stuck? Character disappeared?</dt>

        <dd>Press F5</dd>
    </dl>

    <h3>Viewing changes</h3>

    <p>Press play to see changes that you have made!</p>

    <h3>Pro tip</h3>

    <p>Hold down the shift key before dragging to copy instead of move</p><br>
